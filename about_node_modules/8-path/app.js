const path = require("path");

// 운영체제 별로 경로가 다르므로 직접 입력 X ⭐️

// POSIX (Unix: Mac, Linux) : 'Users/temp/myfile.html'
// Windows : 'C:\\temp\\myfile.html'

// ❌ 직접 작성 하지마세요!! path.join 쓰면 운영체제별로 경로 만들어줌
console.log(path.sep); // 경로 구분자
console.log(path.delimiter); // 환경 변수 구분자

console.log(__dirname);
console.log(__filename);

// basename
console.log(path.basename(__filename)); // file 이름만
console.log(path.basename(__filename, ".js")); // .js 제거 후 file 이름만

// dirname
console.log(path.dirname(__filename));

// extension
console.log(path.extname(__filename)); // 확장자만

// parse, 경로를 파싱(오브젝트형태로)
const parsed = path.parse(__filename);
console.log(parsed);

// format 파싱된 오브젝트 경로 합치기
const str = path.format(parsed);
console.log(str);

// 절대경로인지, 상대경로인지 확인
console.log("isAbsolute?", path.isAbsolute(__dirname));
console.log("isAbsolute?", path.isAbsolute("../"));

// normalize, 경로가 이상하다면 수정해줌
console.log(path.normalize("./forlder//////sub"));

// join, 자동으로 경로 만들어줌
console.log(path.join(__dirname, "image"));
