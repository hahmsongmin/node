const fs = require("fs");

// 조금씩 가져옴

const data = [];
fs.createReadStream("./file.txt", {
  highWaterMark: 8, // defualt 64kbytes
  encoding: "utf-8",
})
  .on("data", (chunk) => {
    data.push(chunk);
    console.count("data");
  })
  .on("end", () => {
    console.log(data.join(""));
  })
  .on("error", (error) => {
    console.log(error);
  });
