const EventEmitter = require("events");
const emitter = new EventEmitter();

const callback1 = (args) => {
  console.log("first - ", args);
};

emitter.on("ivan", callback1);

emitter.on("ivan", (args) => {
  console.log("second - ", args);
});

emitter.on("ivan", (args) => {
  console.log("third - ", args);
});

emitter.emit("ivan", { message: 1 });
emitter.emit("ivan", { message: 2 });
emitter.removeListener("ivan", callback1);
emitter.emit("ivan", { message: 3 });
