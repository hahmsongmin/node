const fs = require('fs');

// 스트림, 스트리밍 조금 조금씩(이벤트 기반)
const data = [];
fs.createReadStream('./file.txt', {
  //   highWaterMark: 8, // 64 kbytes (default)
  //   encoding: 'utf-8',
})
  .on('data', (chunk) => {
    data.push(chunk);
    console.count('data');
  })
  .on('error', (error) => {
    console.error(error);
  })
  .on('end', () => {
    console.log(data.join());
  });
