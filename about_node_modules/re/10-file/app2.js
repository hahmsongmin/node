const fs = require('fs').promises;

// reading a file
fs.readFile('./text-new.txt', 'utf-8') //
  .then((data) => console.log(data))
  .catch(console.error);

// writing a file
fs.writeFile('./text.txt', 'you can do it!!!') //
  .catch(console.error);

// 덮어쓰는게 아니라 추가하고 싶을 때는
fs.appendFile('./text.txt', 'jonber win!') //
  .then(() => {
    // copy
    fs.copyFile('./text.txt', './text2.txt') //
      .then(() => console.log('Done'))
      .catch(console.error);
  })
  .catch(console.error);

// folder
fs.mkdir('sub-folder').catch(console.error);

fs.readdir('./') //
  .then(console.log)
  .catch(console.error);
