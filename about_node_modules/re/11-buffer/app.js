// Fixed-size chuck of memory
// array of integers, byte of data
const fs = require('fs');

const buf = Buffer.from('Hi');
console.log(buf); // <Buffer 48 69> 유니코드 형태
console.log(buf[0], buf[1]); // 72 105 아스키코드 형태
console.log(buf.toString()); // Hi

// create buffer
const buf2 = Buffer.alloc(2); // 초기화해서 줌
const buf3 = Buffer.alloc(2);
buf2[0] = 72;
buf2[1] = 105;
buf2.copy(buf3);
console.log(buf2.toString());
console.log(buf3);

// concat
const newBuf = Buffer.concat([buf, buf2, buf3]); // <Buffer 48 69 48 69 48 69>
console.log(newBuf);
